#include<iostream>
#include<cmath>
using std::cout;

int main()
{
    sqrt(25); //squareroot
    pow(9, 5); //exponent
    remainder(10,3); //remainder after division
    fmax(10,5); //maximum of values
    fmin(19,5); //minimum of values
    ceil(3.25); //round off to next integer
    floor(3.25); //round off to previous integer
    trunc(3.25); //remove part after decimal
    round(3.25); //round off to nearest digit
}